//This is a simple program to show some arithmetic operations on Integers data type

package main

import "fmt"

func main(){
    fmt.Println("Numbers in Go : ")
    fmt.Println("Addition :\n  8 + 2 = ", 8+2)
    fmt.Println("Subtraction : \n  8 - 2 = ", 8-2)
    fmt.Println("Division :\n  8 / 2 = ", 8/2)
    fmt.Println("Multiplication : \n  8 * 2 = ", 8*2)
    fmt.Println("Modulus(Remainder) : \n  8 % 2 = ", 8%2)
}